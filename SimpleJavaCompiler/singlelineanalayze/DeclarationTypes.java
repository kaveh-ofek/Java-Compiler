package oop.ex6.singlelineanalayze;

/**
 * enum class for the possible types of a declaration line
 */
public enum DeclarationTypes
{
    /**
     * int declaration type
     */
    INT,

    /**
     * double declaration type
     */
    DOUBLE,

    /**
     * char declaration type
     */
    CHAR,

    /**
     * boolean declaration type
     */
    BOOLEAN,

    /**
     * string declaration type
     */
    STRING
}

